import os

print(os.name)#nome do tipo de sistema operacional. Os nomes previstos são: 'posix' (Linux, Mac), 'nt' (Windows), 'java'.
print(os.getlogin())#  nome de usuário corrente.
print(os.environ)#variáveis de ambiente do sistema.
print(os.getcwd())#O comando os.getcwd() obtém o caminho (path) completo do diretório corrente.
print(os.getpid())#O comando os.getpid() apresenta o PID (identificador de processo) do próprio processo em execução.

